            {{ __('Phtiriasis') }}
        </h2>

        <p class="indent-8">Phthiriasis merupakan penyakit kulit yang disebabkan oleh infestasi pinjal. Pinjal merupakan
            insekta tanpa sayap,
            berbentuk pipih, memiliki kaki-kaki kuat untuk meloncat. Infestasi pinjal yang banyak merugikan pada kucing
            ada
            beberapa jenis, yaitu yang termasuk familia Sarcopsyllidae.</p>
        <br><strong>Patogenesis</strong><br>
        <p class="indent-8">Pinjal dapat bertindak sebagai vector berbagai agen penyakit. Sudah menjadi kenyataan bahwa
            aktivitas pinjal
            anjing-kucing sangat terkait dengan suhu lingkungan, dimana jika suhu lingkungan panas pinjal akan semakin
            aktif
            bergerak dan menghisap darah.</p><br>
        <strong>Gejala Klinis</strong>
        <img src="public/img/phtiriasis.png" alt="phtiriasis">
        • Menggosok area gigitan <br>
        • Menggigit area gigitan <br>
        • Menggaruk tempat gigitan <br>
        • Kerontokan rambut <br>
        • Kadang-kadang terjadi kelukaan kulit. <br>
        <br><strong>Diagnosis</strong><br>
        <p class="indent-8">Diagnosis didasarkan pada pemeriksaan parasit dibawah mikroskop, dengan melihat bentukan
            morfologi dan pergerakan
            dari parasit tersebut. Dapat pula dengan mengamati rambut hewan. Rambut biasanya kotor, tinja pinjal
            berwarna hitam
            terdapat disela-sela rambut. Bintik merah pada kulit juga terlihat menempel di rambut seperti gigitan
            serangga,
            dimana darah tersebut merupakan makanan bagi larva pinjal. Agar kamu tidak ragu kamu bisa kontrol untuk
            pemeriksaan
            mikroskopis ke Klinik Hewan RAJAPET. </p>
        <br><strong>Treatment/Terapi</strong><br>
        Terapi pengobatan yang utama adalah untuk menghilangkan pinjal dari tubuh hewan, seperti: <br>
        • Check up ke Dokter Hewan <br>
        • Menjaga sanitasi kandang sekitar <br>
        • Grooming rutin <br>
        • Tetes kutu <br>
        • Jangan ajak anjingmu bermain di taman. <br><br>
        <strong>PROVET PASTILLES ANTI PARASIT</strong><br>
        Keunggulan : <br>
        • Protein 11% & lemak 7,1% <br>
        • Mencegah dan mengobati masalah kutu, caplak, pinjal, tungau dan cacing <br>
        • Menurunkan kadar kolesterol <br>
        • Mengandung bahan alami Allicin (diallyl thiosulfinate) <br>
        • Meningkatkan massa otot <br>
        • Mengandung klorofil pada suplemen ini bermanfaat untuk mengatasi anemia serta bersifat sebagai detoksifikasi
        (Membersihkan racun) <br>
        • Mengurangi stress akibat lingkungan baru, rumah baru, ataupun anjing peliharaan baru <br>
        • Mengurangi stress dan perasaan tidak tenang karena birahi <br>
        • Mengurangi gejala gangguan penyakit kulit dermatitis <br>
        • Dapat meningkatkan nafsu makan <br>
        • Memiliki rasa lezat dan tidak pahit sehingga dapat dimakan langsung. <br>

