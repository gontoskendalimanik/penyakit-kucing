Phthiriasis merupakan penyakit kulit yang disebabkan oleh infestasi pinjal. Pinjal merupakan insekta tanpa sayap,
berbentuk pipih, memiliki kaki-kaki kuat untuk meloncat. Infestasi pinjal yang banyak merugikan pada kucing ada beberapa
jenis, yaitu yang termasuk familia Sarcopsyllidae.
Patogenesis
Pinjal dapat bertindak sebagai vector berbagai agen penyakit. Sudah menjadi kenyataan bahwa aktivitas pinjal
anjing-kucing sangat terkait dengan suhu lingkungan, dimana jika suhu lingkungan panas pinjal akan semakin aktif
bergerak dan menghisap darah.
Gejala Klinis

• Menggosok area gigitan
• Menggigit area gigitan
• Menggaruk tempat gigitan
• Kerontokan rambut
• Kadang-kadang terjadi kelukaan kulit.
Diagnosis
Diagnosis didasarkan pada pemeriksaan parasit dibawah mikroskop, dengan melihat bentukan morfologi dan pergerakan dari
parasit tersebut. Dapat pula dengan mengamati rambut hewan. Rambut biasanya kotor, tinja pinjal berwarna hitam terdapat
disela-sela rambut. Bintik merah pada kulit juga terlihat menempel di rambut seperti gigitan serangga, dimana darah
tersebut merupakan makanan bagi larva pinjal. Agar kamu tidak ragu kamu bisa kontrol untuk pemeriksaan mikroskopis ke
Klinik Hewan RAJAPET.
Treatment/Terapi
Terapi pengobatan yang utama adalah untuk menghilangkan pinjal dari tubuh hewan, seperti:
• Check up ke Dokter Hewan
• Menjaga sanitasi kandang sekitar
• Grooming rutin
• Tetes kutu
• Jangan ajak anjingmu bermain di taman.
PROVET PASTILLES ANTI PARASIT
Keunggulan:
• Protein 11% & lemak 7,1%
• Mencegah dan mengobati masalah kutu, caplak, pinjal, tungau dan cacing
• Menurunkan kadar kolesterol
• Mengandung bahan alami Allicin (diallyl thiosulfinate)
• Meningkatkan massa otot
• Mengandung klorofil pada suplemen ini bermanfaat untuk mengatasi anemia serta bersifat sebagai detoksifikasi
(Membersihkan racun)
• Mengurangi stress akibat lingkungan baru, rumah baru, ataupun anjing peliharaan baru
• Mengurangi stress dan perasaan tidak tenang karena birahi
• Mengurangi gejala gangguan penyakit kulit dermatitis
• Dapat meningkatkan nafsu makan
• Memiliki rasa lezat dan tidak pahit sehingga dapat dimakan langsung.
