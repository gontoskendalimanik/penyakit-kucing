Apa itu tungau telinga?
Kutu telinga , Otodectes cynotis , adalah kutu permukaan yang hidup pada kucing, anjing, kelinci, dan musang. Kutu
telinga biasanya ditemukan di liang telinga, tetapi dapat juga hidup di permukaan kulit. Kutu telinga sangat menular,
dan hewan dapat terinfeksi melalui kontak langsung dengan hewan lain yang terinfeksi. Kutu telinga hampir tidak terlihat
oleh mata telanjang dan dapat terlihat sebagai bintik putih yang bergerak pada latar belakang gelap.
Apa siklus hidup tungau telinga?
Diperlukan waktu sekitar 3 minggu bagi tungau untuk berkembang dari telur hingga dewasa, melalui total 5 tahap. Tungau
telinga dewasa hidup sekitar 2 bulan, selama waktu tersebut mereka terus bereproduksi. Seluruh siklus hidup tungau
telinga terjadi pada hewan inangnya, meskipun tungau dapat bertahan hidup dalam waktu terbatas di lingkungan.
Apa tanda klinis tungau telinga?
Kutu telinga merupakan penyebab umum penyakit dan infeksi telinga, meskipun kondisi telinga lainnya dapat menyebabkan
tanda klinis yang serupa. Kutu telinga merupakan ektoparasit (parasit eksternal) kedua yang paling umum ditemukan pada
hewan peliharaan; yang paling umum adalah kutu.
Infestasi merupakan masalah yang sangat umum pada anak anjing dan anak kucing, meskipun hewan peliharaan dari segala
usia dapat terkena dampaknya. Tanda-tanda klinis infestasi bervariasi dalam tingkat keparahannya dari satu hewan
peliharaan ke hewan peliharaan lainnya, dan dapat mencakup kombinasi dari:
1.Iritasi telinga, yang menyebabkan rasa gatal pada telinga atau gelengan kepala.
2.Keluarnya cairan dari telinga berupa kotoran berwarna gelap, lilin, atau kerak.
3.Area kerontokan rambut terjadi akibat trauma diri akibat cakaran anjing atau perawatan berlebihan.
4.Ruam berkerak di sekitar atau di dalam telinga.
5.Hematoma aural (lepuh darah besar pada telinga, disebabkan oleh pecahnya pembuluh darah kecil antara kulit dan tulang
rawan); ini terjadi akibat anjing menggaruk telinganya.
Lesi kulit paling sering menyerang telinga dan kulit di sekitarnya, tetapi kadang-kadang area tubuh lainnya juga dapat
terpengaruh.
Bagaimana infestasi tungau telinga didiagnosis?
Tanda-tanda klinis yang umum dengan riwayat kontak dengan kucing atau anjing lain akan menunjukkan adanya tungau
telinga. Meskipun tungau telinga sering menyebabkan penyakit telinga, kondisi lain dapat mengakibatkan tanda-tanda
klinis yang sangat mirip dan harus disingkirkan sebelum pengobatan dimulai.
Diagnosis dilakukan dengan mengamati tungau. Hal ini biasanya mudah dan dapat dilakukan dengan memeriksa telinga hewan
peliharaan menggunakan otoskop atau dengan pemeriksaan mikroskopis cairan yang keluar dari telinga. Jika telinga terasa
sangat sakit, hewan peliharaan mungkin perlu dibius agar telinga dapat diperiksa dan diobati dengan benar.
Bagaimana tungau telinga diobati?
Dokter hewan akan memberi tahu Anda tentang produk insektisida mana yang cocok. Ada beberapa obat telinga yang
dilisensikan untuk pengobatan tungau telinga pada hewan peliharaan. Tidak ada obat yang dapat menembus telur atau pupa,
jadi pengobatan ditujukan untuk membunuh bentuk dewasa dan larva.
