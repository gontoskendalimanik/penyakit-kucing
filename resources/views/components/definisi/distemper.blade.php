Distemper kucing, yang juga dikenal sebagai virus panleukopenia kucing (FPV) atau parvo kucing, adalah penyakit yang
sangat berbahaya dan terkadang fatal yang menyerang kucing. Penyakit ini juga disebut demam kucing, wabah kucing,
agranulositosis kucing, dan radang usus menular kucing.
Apa pun sebutannya, penyakit ini sangat menular dan menyebar di antara kucing dan dapat ditemukan hampir di mana saja di
lingkungan sekitar. Kucing lebih rentan tertular virus ini jika mereka tinggal di komunitas seperti kandang, tempat
penampungan hewan, toko hewan peliharaan, dan komunitas kucing liar.
Meskipun semua kucing berisiko tertular FPV, anak kucing, kucing dengan sistem kekebalan tubuh yang lemah, dan kucing
yang tidak divaksinasi memiliki risiko lebih besar. Mereka juga dapat terkena dampak yang lebih parah.
Penyakit ini menyerang lapisan usus dan sel darah putih. Meskipun mengancam jiwa, kucing yang selamat akan menjadi kebal
terhadap infeksi berikutnya.
Jumlah kematian yang terkait dengan distemper kucing telah menurun selama bertahun-tahun, berkat kemajuan dalam bidang
kedokteran hewan dan vaksinasi yang efektif. Namun, penyakit ini pernah menjadi penyebab utama kematian kucing, dan
dampaknya masih dapat dilihat dan menghancurkan kucing yang tidak divaksinasi, itulah sebabnya mengapa sangat penting
untuk memvaksinasi kucing Anda.
Distemper kucing merupakan penyakit pertama yang disebabkan oleh virus yang didiagnosis pada kucing, dan parvovirus yang
menyebabkan distemper kucing dapat bertahan hidup hingga satu tahun di lingkungan. Namun, virus ini tidak menyerang
manusia.
Penyebab Distemper Kucing
Parvovirus kucing merupakan penyebab utama panleukopenia. Setelah terinfeksi, kucing dapat menulari kucing lain yang
bersentuhan dengan darah, urin, atau fesesnya. Selain itu, kutu dapat membawa penyakit dari kucing yang terinfeksi ke
kucing lain yang tidak divaksinasi. Manusia juga dapat menularkan penyakit ini jika mereka memegang kucing yang
terinfeksi dan kemudian bersentuhan dengan kucing yang tidak divaksinasi sebelum mencuci tangan atau mengganti
pakaiannya.
Biasanya, kucing yang terinfeksi akan menyebarkan virus hingga dua hari setelah terinfeksi. Namun, bahkan setelah
pembawa virus tidak lagi menular, virus dapat bertahan hidup di permukaan untuk waktu yang lama dan tidak terpengaruh
oleh banyak pembersih rumah tangga, kecuali pemutih.
Kucing yang tidak divaksinasi yang bersentuhan dengan permukaan yang terkontaminasi, seperti alas tidur, kandang, wadah
makanan, kotak pasir, dan permukaan lainnya, berisiko tertular virus, meskipun mereka tidak bersentuhan dengan kucing
yang terinfeksi. Karena alasan ini, kucing yang tidak divaksinasi harus dijauhkan dari area tempat kucing yang
terinfeksi berada.
