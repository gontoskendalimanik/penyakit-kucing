    Apa itu koksidiosis?
    Koksidiosis adalah infeksi saluran usus yang disebabkan oleh organisme bersel tunggal (protozoa) yang disebut
    coccidia. Spesies Isospora adalah jenis coccidia yang paling umum dan terkadang disebut Cystoisospora . Karena
    coccidia hidup di saluran usus dan dapat menyebabkan diare, mereka sering disalahartikan sebagai cacing usus, tetapi
    coccidia bukanlah cacing. Mereka mikroskopis dan bertindak sebagai parasit yang hidup di dalam sel-sel yang melapisi
    usus.
    Bagaimana kucing saya terinfeksi?
    Kucing Anda kemungkinan besar terinfeksi coccidia karena menelan oosit (koksidia yang belum matang) yang ditemukan
    dalam feses kucing dan lingkungan apa pun yang terkontaminasi feses. Oosit dikeluarkan melalui feses kucing yang
    terinfeksi. Oosit hidup di lingkungan dan akhirnya tumbuh menjadi oosit yang lebih berkembang yang dapat menyebabkan
    infeksi atau infeksi ulang. Pematangan ini dapat terjadi hanya dalam waktu delapan jam tetapi biasanya memakan waktu
    tujuh hingga sepuluh hari. Bentuk oosit yang lebih matang juga menular ke mamalia lain, seperti tikus. Jika kucing
    Anda menelan oosit, oosit tersebut akan matang di usus untuk melengkapi siklus hidupnya. Jika tikus menelan oosit,
    oosit tersebut akan membentuk kista di organ tikus, dan kucing Anda dapat terinfeksi dengan memakan tikus yang
    terinfeksi.
    Anak kucing biasanya didiagnosis dengan koksidiosis, bukan sejak lahir. Setelah lahir, anak kucing dapat terinfeksi
    oleh induknya melalui paparan kotorannya, terutama karena anak kucing cenderung suka menjelajah. Koksidiosis sangat
    berbahaya bagi anak kucing karena sistem kekebalan tubuhnya belum berkembang. Jika induknya mengeluarkan oosit,
    bayinya dapat menelan oosit tersebut saat menyusui.
    Karena anak kucing (berusia kurang dari enam bulan) tidak memiliki kekebalan terhadap coccidia, organisme tersebut
    dapat berkembang biak dalam jumlah besar, yang dapat menimbulkan konsekuensi serius. Penyakit ini berkembang dalam
    waktu sekitar 3-11 hari setelah anak kucing pertama kali menelan coccidia. Akibatnya, anak kucing yang sakit akibat
    infeksi coccidia berusia setidaknya dua minggu.
    Meskipun sebagian besar kasus disebabkan oleh infeksi dari induknya, hal ini tidak selalu terjadi. Anak kucing yang
    terinfeksi dapat menular ke anak kucing lainnya. Di tempat pengembangbiakan, tempat penampungan hewan, kandang, dan
    area lain tempat banyak hewan peliharaan dapat berdekatan, sebaiknya pisahkan hewan yang terinfeksi dari hewan yang
    tidak terinfeksi.
    Apa tanda-tanda klinis koksidiosis?
    Banyak kucing yang terinfeksi coccidia tidak mengalami diare atau tanda klinis lainnya. Bila oosit ditemukan dalam
    tinja kucing tanpa diare, umumnya dianggap sebagai temuan sementara dan tidak signifikan. Namun, pada anak kucing
    dan kucing dewasa yang lemah, coccidiosis dapat menyebabkan diare encer atau berlendir yang parah, dehidrasi,
    gangguan perut, kehilangan nafsu makan, dan muntah. Dalam kasus yang parah, kematian dapat terjadi.
    Bagaimana koksidiosis didiagnosis?
    Koksidiosis didiagnosis dengan melakukan pemeriksaan mikroskopis pada sampel tinja. Infeksi dengan beberapa parasit
    koksidiosis yang kurang umum dapat didiagnosis dengan tes darah khusus.
    Bagaimana koksidiosis diobati?
    Obat yang paling umum digunakan untuk mengobati koksidiosis adalah antibiotik golongan sulfa, sulfadimethoxine
    (Albon®). Obat ini biasanya diberikan selama 5-20 hari. Beberapa formula obat ini memiliki rasa yang enak dan
    sebagian besar kucing akan menerimanya dengan mudah. ​​Jika obat sulfa tidak efektif, tersedia pengobatan lain,
    termasuk antibiotik sulfa yang dikombinasikan dengan obat lain, amprolium (Albac/Amprol Hi-E®), atau penggunaan
    ponazuril (Marquis®) atau toltrazuril (Baycox®) yang tidak sesuai label. Pengobatan suportif tambahan mungkin
    diperlukan jika terjadi diare dan dehidrasi.
    "Kucing sering terinfeksi ulang dari lingkungan..."
    Kucing sering terinfeksi ulang dari lingkungan, jadi disinfeksi penting dilakukan. Oosit sangat tahan terhadap
    kondisi lingkungan dan disinfektan. Penggunaan pemutih klorin encer, satu cangkir (250 ml) pemutih dicampur dengan
    satu galon (3,8 L) air, efektif. Pastikan untuk menguji pembersihan area kecil dari bahan yang terkena karena
    pemutih dapat merusak banyak permukaan. Pembersihan uap juga dapat digunakan untuk menghancurkan oosit. Pastikan
    untuk membuang feses secepat mungkin dari lingkungan untuk mencegah infeksi ulang.
