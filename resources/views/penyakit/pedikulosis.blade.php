    Penyakit ini menyerang kitten (anak kucing) maupun kucing dewasa. Umumnya penularan disebabkan karena terjatuhnya
    kutu (lice) yang kemudian terbawa oleh hewan saat berjalan.
    Pengobatan yang dapat dilakukan yaitu memberikan Frontline / Selamectin spot on (obat penghilang tungau/kutu),
    vitamin megaderm (vitamin untuk bulu), dan mandi kutu.
    Berikut adalah beberapa gejala pedikulosis pada kucing:
    Gatal-gatal dan kegelisahan
    Menggaruk, menggosok, dan menggigit area yang terinfeksi
    Bulu yang kasar, tidak merata, dan kering
    Rambut yang kusut tebal
    Papula dan kerak kecil
    Anemia dan kelemahan pada infestasi yang parah
    Pedikulosis pada kucing dapat diobati dengan obat anti ektoparasit. Anda dapat menggunakan sampo, kalung, semprotan,
    atau debu yang membunuh kutu. Jangan gunakan insektisida pada kucing tanpa membicarakannya dengan dokter hewan.
    Pediculosis adalah penyakit kulit pada kucing yang disebabkan oleh kutu (lice). Pada umumnya.
